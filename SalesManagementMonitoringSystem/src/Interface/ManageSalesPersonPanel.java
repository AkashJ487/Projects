    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        if((userNameTextField.getText().isEmpty()) || (nameTextField.getText().isEmpty()) ||
            (addressTextField.getText().isEmpty())  || (emailTextField.getText().isEmpty()))
        JOptionPane.showMessageDialog(null, "Please Enter the required details", "Error", 0);
        else
        {
            if(temp==1)
            {
                SalesPerson s = (SalesPerson)salesPersonComboBox.getSelectedItem();
                UserAccount userAccount = s.getUserAccount();
                userAccount.setUserName(userNameTextField.getText());
                userAccount.setRole("SalesPerson");
                Person p = userAccount.getPerson();
                p.setName(nameTextField.getText());
                p.setAddress(addressTextField.getText());
                p.setEmail(emailTextField.getText());
                if(activeRadioButton.isSelected())
                userAccount.setStatus('Y');
                else
                userAccount.setStatus('N');
                JOptionPane.showMessageDialog(null, "SalesPerson Updated successfully");
                updateButton.setEnabled(true);
            }
            else
            {
                SalesPerson s=business.getSalesPersonsDirectory().addSalesPerson();
                UserAccount ua = business.getUserDirectory().addUser();
                Person p = ua.getPerson();
                p.setName(nameTextField.getText());
                p.setAddress(addressTextField.getText());
                p.setEmail(emailTextField.getText());
                ua.setUserName(userNameTextField.getText());
                ua.setRole("SalesPerson");
                ua.setPassword("pass".hashCode());
                s.setUserAccount(ua);
                JOptionPane.showMessageDialog(null, "SalesPerson Added successfully");
                addButton.setEnabled(true);
            }
            reload();
        }
    }//GEN-LAST:event_saveButtonActionPerformed

