    private void reload()
    {
        temp=0;
        salesPersonComboBox.removeAllItems();
        for(SalesPerson s:business.getSalesPersonsDirectory().getListOfSalesPersons())
        {
            salesPersonComboBox.addItem(s);
        }
        saveButton.setEnabled(false);
        nameTextField.setEnabled(false);
        emailTextField.setEnabled(false);
        addressTextField.setEnabled(false);
        userNameTextField.setEnabled(false);
        activeRadioButton.setEnabled(false);
        disabledsRadioButton.setEnabled(false);
        populateSalesPerson();
    }
    private void populateSalesPerson()
    {
        SalesPerson s = (SalesPerson)salesPersonComboBox.getSelectedItem();
        UserAccount ua = s.getUserAccount();
        Person p = ua.getPerson();
        nameTextField.setText(p.getName());
        addressTextField.setText(p.getAddress());
        emailTextField.setText(p.getEmail());
        userNameTextField.setText(ua.getUserName());
        roleTextField.setText(ua.getRole());
        if(ua.getStatus()=='Y')
            activeRadioButton.setSelected(true);
        else
            disabledsRadioButton.setSelected(true);
    }
