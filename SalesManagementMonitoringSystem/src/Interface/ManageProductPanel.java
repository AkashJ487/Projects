    private void srchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_srchButtonActionPerformed
        // TODO add your handling code here:
        if(srchTextField.getText().isEmpty())
            JOptionPane.showMessageDialog(null, "Please enter product name before search", "Error", 0);
        else
        {
            DefaultTableModel dtm = (DefaultTableModel) productTable.getModel();
            dtm.setRowCount(0);
            for(Product p: supplier.getProductCatlog().getListOfProducts())
            {
                if(p.getName().equalsIgnoreCase(srchTextField.getText()))
                {
                    Object row[]=new Object[2];
                    row[0]=p;
                    row[1]=p.getProductId();
                    dtm.addRow(row);
                }
            }
        }
    }//GEN-LAST:event_srchButtonActionPerformed

    private void viewButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = productTable.getSelectedRow();
        if (selectedRow < 0 )
            JOptionPane.showMessageDialog(null, "Please select a row");
        else
        {
            Product p = (Product)productTable.getValueAt(selectedRow, 0);
            try
            {
                nameTextField.setText(p.getName());
                idTextField.setText(String.valueOf(p.getProductId()));
                priceTextField.setText(String.valueOf(p.getBasePrice()));
                updateButton.setEnabled(true);    
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, "Price is a numeric field", "Error", 0);
            }
        }
    }//GEN-LAST:event_viewButtonActionPerformed

