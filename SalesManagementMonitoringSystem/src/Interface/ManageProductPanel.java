    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        if(nameTextField.getText().isEmpty() || priceTextField.getText().isEmpty())
            JOptionPane.showMessageDialog(null, "Please enter details in all fields", "Error", 0);
        else
        {
                if(temp==1)
                {
                    int selectedRow = productTable.getSelectedRow();
                    Product p = (Product)productTable.getValueAt(selectedRow, 0);
                    try
                    {
                        p.setName(nameTextField.getText());
                        p.setBasePrice(Double.parseDouble(priceTextField.getText()));
                        JOptionPane.showMessageDialog(null,"Product updated successfully");
                        updateButton.setEnabled(true);
                        productTable.setEnabled(true);
                    }
                    catch(Exception e)
                    {
                        JOptionPane.showMessageDialog(null, "Price is a numeric field", "Error", 0);
                    }
                }
                else
                {
                    Product p=supplier.getProductCatlog().addProduct();
                    p.setProductCatlog(supplier.getProductCatlog());
                    p.setName(nameTextField.getText());
                    p.setBasePrice(Double.parseDouble(priceTextField.getText()));
                    JOptionPane.showMessageDialog(null,"Product added successfully");
                    addButton.setEnabled(true);
                }
            reload();
        }
    }//GEN-LAST:event_saveButtonActionPerformed

